public class TwoNumbers {
    public static void main(String[] args) {
        int firstNumber = 22;
        int secondNumber = 13;

        System.out.println(firstNumber + secondNumber);
        System.out.println(firstNumber - secondNumber);
        System.out.println(firstNumber * secondNumber);
        System.out.println((double)firstNumber / secondNumber);
        System.out.println(firstNumber / secondNumber);
        System.out.println(firstNumber % secondNumber);
    }
}
